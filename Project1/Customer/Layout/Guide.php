    </p>
    <p>
      <b>7.2. Nghĩa vụ của hội sách trực tuyến</b>
    </p>
    <p>
      a) Chịu trách nhiệm xây dựng hội sách trực tuyến bao gồm việc nghiên cứu, thiết kế, xây dựng, mua sắm các thiết bị phần cứng, phần mềm, kết nối Internet, hệ thống nhân sự hỗ trợ phục vụ, các chính sách cụ thể cho hoạt động của hội sách trực tuyến.
    </p>
    <p>
      b) Tiến hành triển khai và hợp tác với các đối tác trong việc xây dựng hệ thống các dịch vụ, các công cụ tiện ích phục vụ cho việc giao dịch của các thành viên tham gia và người sử dụng.
    </p>
    <p>
      c) Chịu trách nhiệm định hướng, xây dựng, bổ sung hệ thống các kiến thức, thông tin về: nghiệp vụ ngoại thương, thương mại điện tử, thanh toán điện tử, hệ thống văn bản pháp luật thương mại trong nước và quốc tế, thị trường nước ngoài, cũng như các tin tức có liên quan đến hoạt động của hội sách trực tuyến.
    </p>
    <p>
      d) Duy trì hoạt động bình thường và khắc phục các sự cố như: sự cố kỹ thuật về máy móc, lỗi phần mềm, hệ thống đường truyền internet, nhân sự, các biến động xã hội, thiên tai, mất điện, các quyết định của cơ quan nhà nước hay một tổ chức liên quan khác.
    </p>
    <p>
      e) Tiếp nhận, xử lý và giải đáp những khiếu nại, thắc mắc của thành viên.
    </p>
    <p>
      8. GIỚI HẠN TRÁCH NHIỆM CỦA HỘI SÁCH TRỰC TUYẾN.
    </p>
    <p>
      8.1. Hội sách trực tuyến cam kết nỗ lực đảm bảo sự an toàn và ổn định của toàn bộ hệ thống kỹ thuật.
    </p>
    <p>
      8.2. Hội sách trực tuyến sẽ không chịu trách nhiệm đối với Người Mua và Người Bán nếu xảy ra các thiệt hại bắt nguồn từ các nguyên nhân nằm ngoài kiểm soát gây ra như các sự kiện thiên tai, hỏa hoạn, cháy nổ, sự cố đường truyền internet, chiến tranh, phong tỏa, hoặc các quyết định của cơ quan Nhà nước có thẩm quyền.
