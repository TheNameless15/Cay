      g) Người Bán có trách nhiệm phối hợp với hội sách trực tuyến trong việc giải đáp những khiếu nại, thắc mắc của Người Mua.
    </p>
    <p>
      7. QUYỀN VÀ NGHĨA VỤ CỦA HỘI SÁCH TRỰC TUYẾN.
    </p>
    <p>
      7.1. Quyền của hội sách trực tuyến
    </p>
    <p>
      a) Hội sách trực tuyến sẽ tổ chức thực hiện quảng cáo các chương trình khuyến mại, giới thiệu, cung cấp các sản phẩm, dịch vụ của các Người Bán khác nhau với mức giá ưu đãi đến cộng đồng thành viên của hội sách trực tuyến.
    </p>
    <p>
      b) Hội sách trực tuyến có quyền yêu cầu các thành viên cung cấp thông tin đầy đủ và chính xác. Trong trường hợp có cơ sở để chứng minh thành viên cung cấp thông tin cho hội sách trực tuyến không chính xác, sai lệch, không đầy đủ hoặc vi phạm pháp luật về thuần phong mỹ tục Việt Nam hoặc vi phạm các Hành vi bị cấm đối với Người Bán thì hội sách trực tuyến có quyền từ chối truy cập, tạm ngưng hoặc chấm dứt quyền thành viên. Trong trường hợp chấm dứt quyền thành viên và quyền sử dụng dịch vụ thì tất cả các chứng nhận, các quyền của thành viên được cấp sẽ mặc nhiên hết giá trị, đồng thời thành viên phải chịu trách nhiệm pháp lý về các thông tin có liên quan đến sản phẩm dịch vụ khuyến mại.
    </p>
    <p>
      c) Hội sách trực tuyến có quyền sử dụng mọi thông tin do các thành viên cung cấp nhằm mục đích quảng bá về các hoạt động của hội sách trực tuyến mà không cần thông báo trước tới từng thành viên.
